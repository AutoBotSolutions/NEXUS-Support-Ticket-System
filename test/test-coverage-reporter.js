/**
 * Test Coverage Reporter
 * 
 * Comprehensive test coverage reporting system for the NEXUS platform.
 * Generates detailed coverage reports with metrics and analysis.
 */

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

class TestCoverageReporter {
  constructor() {
    this.coverageData = {
      summary: {},
      detailed: {},
      trends: [],
      thresholds: {
        global: {
          statements: 80,
          branches: 80,
          functions: 80,
          lines: 80
        },
        controllers: {
          statements: 85,
          branches: 85,
          functions: 85,
          lines: 85
        },
        models: {
          statements: 85,
          branches: 85,
          functions: 85,
          lines: 85
        },
        services: {
          statements: 85,
          branches: 85,
          functions: 85,
          lines: 85
        },
        routes: {
          statements: 80,
          branches: 80,
          functions: 80,
          lines: 80
        }
      }
    };
  }

  async generateCoverageReport() {
    console.log('🔍 Generating Test Coverage Report...\n');

    try {
      // Run Jest with coverage
      await this.runJestWithCoverage();
      
      // Parse coverage data
      await this.parseCoverageData();
      
      // Generate HTML report
      await this.generateHtmlReport();
      
      // Generate JSON report
      await this.generateJsonReport();
      
      // Generate markdown report
      await this.generateMarkdownReport();
      
      // Analyze trends
      await this.analyzeCoverageTrends();
      
      // Check thresholds
      await this.checkCoverageThresholds();
      
      console.log('✅ Test Coverage Report Generated Successfully!\n');
      console.log('📊 Coverage Reports Available:');
      console.log('   - HTML Report: coverage/lcov-report/index.html');
      console.log('   - JSON Report: coverage/coverage-report.json');
      console.log('   - Markdown Report: coverage/COVERAGE_REPORT.md');
      
      return this.coverageData;
    } catch (error) {
      console.error('❌ Error generating coverage report:', error.message);
      throw error;
    }
  }

  async runJestWithCoverage() {
    console.log('🧪 Running Jest with coverage collection...');
    
    try {
      // Run Jest with coverage
      const result = execSync('npm test -- --coverage --coverageReporters=json,text --coverageDirectory=coverage', {
        cwd: process.cwd(),
        encoding: 'utf8',
        stdio: 'pipe'
      });
      
      console.log('✅ Jest coverage collection completed');
    } catch (error) {
      console.error('❌ Jest coverage collection failed:', error.message);
      throw error;
    }
  }

  async parseCoverageData() {
    console.log('📊 Parsing coverage data...');
    
    try {
      // Read coverage JSON
      const coverageJsonPath = path.join(process.cwd(), 'coverage', 'coverage-summary.json');
      
      if (!fs.existsSync(coverageJsonPath)) {
        // Try alternative location
        const altPath = path.join(process.cwd(), 'coverage', 'coverage-final.json');
        if (fs.existsSync(altPath)) {
          this.coverageData.summary = JSON.parse(fs.readFileSync(altPath, 'utf8'));
        } else {
          throw new Error('Coverage summary file not found');
        }
      } else {
        this.coverageData.summary = JSON.parse(fs.readFileSync(coverageJsonPath, 'utf8'));
      }
      
      // Read detailed coverage
      const coverageDetailPath = path.join(process.cwd(), 'coverage', 'coverage-final.json');
      if (fs.existsSync(coverageDetailPath)) {
        this.coverageData.detailed = JSON.parse(fs.readFileSync(coverageDetailPath, 'utf8'));
      }
      
      console.log('✅ Coverage data parsed successfully');
    } catch (error) {
      console.error('❌ Error parsing coverage data:', error.message);
      throw error;
    }
  }

  async generateHtmlReport() {
    console.log('📄 Generating HTML coverage report...');
    
    try {
      // HTML report is generated by Jest automatically
      const htmlPath = path.join(process.cwd(), 'coverage', 'lcov-report', 'index.html');
      
      if (fs.existsSync(htmlPath)) {
        console.log('✅ HTML report generated at: coverage/lcov-report/index.html');
      } else {
        console.log('⚠️  HTML report not found, Jest may need to be configured for HTML reporting');
      }
    } catch (error) {
      console.error('❌ Error generating HTML report:', error.message);
    }
  }

  async generateJsonReport() {
    console.log('📋 Generating JSON coverage report...');
    
    try {
      const reportData = {
        generatedAt: new Date().toISOString(),
        summary: this.coverageData.summary,
        detailed: this.coverageData.detailed,
        thresholds: this.coverageData.thresholds,
        analysis: this.analyzeCoverageSummary()
      };
      
      const jsonPath = path.join(process.cwd(), 'coverage', 'coverage-report.json');
      fs.writeFileSync(jsonPath, JSON.stringify(reportData, null, 2));
      
      console.log('✅ JSON report generated at: coverage/coverage-report.json');
    } catch (error) {
      console.error('❌ Error generating JSON report:', error.message);
    }
  }

  async generateMarkdownReport() {
    console.log('📝 Generating Markdown coverage report...');
    
    try {
      const analysis = this.analyzeCoverageSummary();
      const markdown = this.buildMarkdownReport(analysis);
      
      const mdPath = path.join(process.cwd(), 'coverage', 'COVERAGE_REPORT.md');
      fs.writeFileSync(mdPath, markdown);
      
      console.log('✅ Markdown report generated at: coverage/COVERAGE_REPORT.md');
    } catch (error) {
      console.error('❌ Error generating Markdown report:', error.message);
    }
  }

  analyzeCoverageSummary() {
    const summary = this.coverageData.summary;
    const analysis = {
      overall: {
        total: this.calculateOverallCoverage(summary.total),
        status: this.getCoverageStatus(summary.total, this.coverageData.thresholds.global)
      },
      byCategory: {},
      criticalFiles: [],
      recommendations: []
    };

    // Analyze by category
    Object.keys(summary).forEach(category => {
      if (category !== 'total' && summary[category]) {
        const categoryData = summary[category];
        const threshold = this.coverageData.thresholds[category] || this.coverageData.thresholds.global;
        
        analysis.byCategory[category] = {
          coverage: this.calculateOverallCoverage(categoryData),
          status: this.getCoverageStatus(categoryData, threshold),
          lines: categoryData.lines || { covered: 0, total: 0 },
          functions: categoryData.functions || { covered: 0, total: 0 },
          branches: categoryData.branches || { covered: 0, total: 0 },
          statements: categoryData.statements || { covered: 0, total: 0 }
        };
      }
    });

    // Find critical files (low coverage)
    if (this.coverageData.detailed) {
      analysis.criticalFiles = this.findCriticalFiles(this.coverageData.detailed);
    }

    // Generate recommendations
    analysis.recommendations = this.generateRecommendations(analysis);

    return analysis;
  }

  calculateOverallCoverage(data) {
    if (!data) return { statements: 0, branches: 0, functions: 0, lines: 0 };
    
    return {
      statements: data.statements ? (data.statements.covered / data.statements.total) * 100 : 0,
      branches: data.branches ? (data.branches.covered / data.branches.total) * 100 : 0,
      functions: data.functions ? (data.functions.covered / data.functions.total) * 100 : 0,
      lines: data.lines ? (data.lines.covered / data.lines.total) * 100 : 0
    };
  }

  getCoverageStatus(data, thresholds) {
    const coverage = this.calculateOverallCoverage(data);
    
    if (coverage.statements < thresholds.statements ||
        coverage.branches < thresholds.branches ||
        coverage.functions < thresholds.functions ||
        coverage.lines < thresholds.lines) {
      return 'FAIL';
    } else if (coverage.statements < thresholds.statements + 10 ||
               coverage.branches < thresholds.branches + 10 ||
               coverage.functions < thresholds.functions + 10 ||
               coverage.lines < thresholds.lines + 10) {
      return 'WARN';
    } else {
      return 'PASS';
    }
  }

  findCriticalFiles(detailedCoverage) {
    const criticalFiles = [];
    
    Object.keys(detailedCoverage).forEach(filePath => {
      const fileData = detailedCoverage[filePath];
      const coverage = this.calculateOverallCoverage(fileData);
      
      // Files with less than 50% coverage are critical
      if (coverage.statements < 50 || coverage.branches < 50 || coverage.functions < 50 || coverage.lines < 50) {
        criticalFiles.push({
          path: filePath,
          coverage: coverage,
          status: 'CRITICAL'
        });
      } else if (coverage.statements < 70 || coverage.branches < 70 || coverage.functions < 70 || coverage.lines < 70) {
        criticalFiles.push({
          path: filePath,
          coverage: coverage,
          status: 'WARNING'
        });
      }
    });
    
    return criticalFiles.sort((a, b) => {
      const aCoverage = a.coverage.statements;
      const bCoverage = b.coverage.statements;
      return aCoverage - bCoverage;
    }).slice(0, 10); // Top 10 critical files
  }

  generateRecommendations(analysis) {
    const recommendations = [];
    
    // Overall coverage recommendations
    if (analysis.overall.status === 'FAIL') {
      recommendations.push({
        priority: 'HIGH',
        category: 'Overall Coverage',
        message: 'Overall coverage is below threshold. Focus on improving test coverage across all categories.',
        action: 'Add unit tests for uncovered code paths'
      });
    } else if (analysis.overall.status === 'WARN') {
      recommendations.push({
        priority: 'MEDIUM',
        category: 'Overall Coverage',
        message: 'Overall coverage is close to threshold but could be improved.',
        action: 'Add more comprehensive tests to reach optimal coverage'
      });
    }
    
    // Category-specific recommendations
    Object.keys(analysis.byCategory).forEach(category => {
      const categoryData = analysis.byCategory[category];
      
      if (categoryData.status === 'FAIL') {
        recommendations.push({
          priority: 'HIGH',
          category: category,
          message: `${category} coverage is below threshold (${categoryData.coverage.statements.toFixed(1)}%).`,
          action: `Add more tests for ${category} components`
        });
      }
    });
    
    // Critical files recommendations
    analysis.criticalFiles.forEach(file => {
      if (file.status === 'CRITICAL') {
        recommendations.push({
          priority: 'HIGH',
          category: 'Critical Files',
          message: `File ${file.path} has very low coverage (${file.coverage.statements.toFixed(1)}%).`,
          action: 'Add comprehensive tests for this file'
        });
      }
    });
    
    return recommendations.slice(0, 10); // Top 10 recommendations
  }

  buildMarkdownReport(analysis) {
    const timestamp = new Date().toISOString();
    
    return `# NEXUS Test Coverage Report

## Executive Summary

**Generated**: ${timestamp}  
**Overall Coverage**: ${analysis.overall.total.statements.toFixed(1)}%  
**Status**: ${this.getStatusBadge(analysis.overall.status)}

### Coverage Overview

| Metric | Coverage | Status |
|--------|----------|--------|
| Statements | ${analysis.overall.total.statements.toFixed(1)}% | ${this.getStatusBadge(this.getCoverageStatus(analysis.overall.total, this.coverageData.thresholds.global))} |
| Branches | ${analysis.overall.total.branches.toFixed(1)}% | ${this.getStatusBadge(this.getCoverageStatus(analysis.overall.total, this.coverageData.thresholds.global))} |
| Functions | ${analysis.overall.total.functions.toFixed(1)}% | ${this.getStatusBadge(this.getCoverageStatus(analysis.overall.total, this.coverageData.thresholds.global))} |
| Lines | ${analysis.overall.total.lines.toFixed(1)}% | ${this.getStatusBadge(this.getCoverageStatus(analysis.overall.total, this.coverageData.thresholds.global))} |

## Coverage by Category

${Object.keys(analysis.byCategory).map(category => {
  const data = analysis.byCategory[category];
  return `### ${category.charAt(0).toUpperCase() + category.slice(1)}

| Metric | Coverage | Status |
|--------|----------|--------|
| Statements | ${data.statements.toFixed(1)}% | ${this.getStatusBadge(data.status)} |
| Branches | ${data.branches.toFixed(1)}% | ${this.getStatusBadge(data.status)} |
| Functions | ${data.functions.toFixed(1)}% | ${this.getStatusBadge(data.status)} |
| Lines | ${data.lines.toFixed(1)}% | ${this.getStatusBadge(data.status)} |

**Details**:
- Lines: ${data.lines.covered}/${data.lines.total}
- Functions: ${data.functions.covered}/${data.functions.total}
- Branches: ${data.branches.covered}/${data.branches.total}
- Statements: ${data.statements.covered}/${data.statements.total}
`;
}).join('\n\n')}

## Critical Files

${analysis.criticalFiles.length > 0 ? `
| File | Coverage | Status |
|------|----------|--------|
${analysis.criticalFiles.map(file => 
  `| ${file.path} | ${file.coverage.statements.toFixed(1)}% | ${this.getStatusBadge(file.status)} |`
).join('\n')}
` : 'No critical files found. All files have adequate coverage.'}

## Recommendations

${analysis.recommendations.map(rec => 
  `### ${rec.priority} - ${rec.category}

**Issue**: ${rec.message}  
**Action**: ${rec.action}
`).join('\n\n')}

## Coverage Thresholds

| Category | Statements | Branches | Functions | Lines |
|----------|------------|----------|-----------|-------|
| Global | ${this.coverageData.thresholds.global.statements}% | ${this.coverageData.thresholds.global.branches}% | ${this.coverageData.thresholds.global.functions}% | ${this.coverageData.thresholds.global.lines}% |
| Controllers | ${this.coverageData.thresholds.controllers.statements}% | ${this.coverageData.thresholds.controllers.branches}% | ${this.coverageData.thresholds.controllers.functions}% | ${this.coverageData.thresholds.controllers.lines}% |
| Models | ${this.coverageData.thresholds.models.statements}% | ${this.coverageData.thresholds.models.branches}% | ${this.coverageData.thresholds.models.functions}% | ${this.coverageData.thresholds.models.lines}% |
| Services | ${this.coverageData.thresholds.services.statements}% | ${this.coverageData.thresholds.services.branches}% | ${this.coverageData.thresholds.services.functions}% | ${this.coverageData.thresholds.services.lines}% |
| Routes | ${this.coverageData.thresholds.routes.statements}% | ${this.coverageData.thresholds.routes.branches}% | ${this.coverageData.thresholds.routes.functions}% | ${this.coverageData.thresholds.routes.lines}% |

## How to Improve Coverage

1. **Run Tests with Coverage**: \`npm test -- --coverage\`
2. **View Detailed Report**: Open \`coverage/lcov-report/index.html\` in your browser
3. **Focus on Critical Files**: Start with files marked as CRITICAL above
4. **Add Unit Tests**: Write tests for uncovered functions and branches
5. **Add Integration Tests**: Test component interactions
6. **Add E2E Tests**: Test complete user workflows

## Coverage Trends

Coverage trends analysis will be available after multiple report generations.

---

*This report was generated automatically by the NEXUS Test Coverage Reporter.*`;
  }

  getStatusBadge(status) {
    const badges = {
      PASS: '✅ PASS',
      WARN: '⚠️ WARN',
      FAIL: '❌ FAIL'
    };
    return badges[status] || status;
  }

  async analyzeCoverageTrends() {
    console.log('📈 Analyzing coverage trends...');
    
    try {
      // Load previous coverage data if available
      const trendsPath = path.join(process.cwd(), 'coverage', 'trends.json');
      
      if (fs.existsSync(trendsPath)) {
        const previousData = JSON.parse(fs.readFileSync(trendsPath, 'utf8'));
        
        // Calculate trends
        const currentCoverage = this.coverageData.summary.total;
        const previousCoverage = previousData.total;
        
        const trends = {
          statements: currentCoverage.statements ? (currentCoverage.statements.pct || 0) - (previousCoverage.statements?.pct || 0) : 0,
          branches: currentCoverage.branches ? (currentCoverage.branches.pct || 0) - (previousCoverage.branches?.pct || 0) : 0,
          functions: currentCoverage.functions ? (currentCoverage.functions.pct || 0) - (previousCoverage.functions?.pct || 0) : 0,
          lines: currentCoverage.lines ? (currentCoverage.lines.pct || 0) - (previousCoverage.lines?.pct || 0) : 0
        };
        
        this.coverageData.trends = trends;
        
        // Save current data for next comparison
        fs.writeFileSync(trendsPath, JSON.stringify(this.coverageData.summary, null, 2));
        
        console.log('✅ Coverage trends analyzed');
      } else {
        // Save current data for future comparison
        fs.writeFileSync(trendsPath, JSON.stringify(this.coverageData.summary, null, 2));
        console.log('📊 First coverage report saved for trend analysis');
      }
    } catch (error) {
      console.error('❌ Error analyzing coverage trends:', error.message);
    }
  }

  async checkCoverageThresholds() {
    console.log('🔍 Checking coverage thresholds...');
    
    const analysis = this.analyzeCoverageSummary();
    let thresholdPassed = true;
    
    // Check global thresholds
    if (analysis.overall.status === 'FAIL') {
      thresholdPassed = false;
      console.log('❌ Global coverage thresholds not met');
    } else {
      console.log('✅ Global coverage thresholds met');
    }
    
    // Check category thresholds
    Object.keys(analysis.byCategory).forEach(category => {
      const categoryData = analysis.byCategory[category];
      if (categoryData.status === 'FAIL') {
        thresholdPassed = false;
        console.log(`❌ ${category} coverage thresholds not met`);
      } else {
        console.log(`✅ ${category} coverage thresholds met`);
      }
    });
    
    // Generate threshold report
    const thresholdReport = {
      passed: thresholdPassed,
      overall: analysis.overall.status,
      categories: Object.keys(analysis.byCategory).map(cat => ({
        category: cat,
        status: analysis.byCategory[cat].status
      })),
      recommendations: analysis.recommendations
    };
    
    const thresholdPath = path.join(process.cwd(), 'coverage', 'threshold-report.json');
    fs.writeFileSync(thresholdPath, JSON.stringify(thresholdReport, null, 2));
    
    console.log(`📊 Threshold report saved to: coverage/threshold-report.json`);
    
    return thresholdPassed;
  }

  async generateSummaryReport() {
    const analysis = this.analyzeCoverageSummary();
    
    return {
      timestamp: new Date().toISOString(),
      overall: {
        coverage: analysis.overall.total,
        status: analysis.overall.status
      },
      categories: analysis.byCategory,
      criticalFiles: analysis.criticalFiles,
      recommendations: analysis.recommendations,
      thresholds: this.coverageData.thresholds
    };
  }
}

// Export for use in other modules
module.exports = TestCoverageReporter;

// Run if called directly
if (require.main === module) {
  const reporter = new TestCoverageReporter();
  reporter.generateCoverageReport()
    .then(() => {
      console.log('\n🎉 Coverage reporting completed!');
      process.exit(0);
    })
    .catch(error => {
      console.error('\n💥 Coverage reporting failed:', error.message);
      process.exit(1);
    });
}
